    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int ri=rTable.getSelectedRow();
        if(ri!=-1){
            try{
                int x=JOptionPane.showConfirmDialog(null,"Do u really wanna Delete","Deletion Confirmation", JOptionPane.YES_NO_OPTION);
                if(x==JOptionPane.YES_OPTION){
                    Integer pid=(Integer)rTable.getValueAt(ri, 0);
                    db.DbConnect.st.executeUpdate("delete from payments where pid='"+pid+"'");
                    getEntries();
                    JOptionPane.showMessageDialog(null, "Successfully Deleted!");
                }
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Plz Select the room first!");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

