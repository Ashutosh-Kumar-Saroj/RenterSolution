    void getEntries() {
        try {
            javax.swing.table.DefaultTableModel dtm
                    = (javax.swing.table.DefaultTableModel) rTable.getModel();
            int rc = dtm.getRowCount();
            while (rc-- != 0) {
                dtm.removeRow(0);
            }
            String ri=(String)renterID.getSelectedItem();
            int rid=Integer.parseInt(ri);
            db.DbConnect.getPaymentsByID.setInt(1,rid);
            ResultSet rs = db.DbConnect.getPaymentsByID.executeQuery();
            while (rs.next()) {
                java.util.Vector r = new java.util.Vector();
                r.add(rs.getInt("pid"));
                r.add(rs.getInt("rid"));
                r.add(rs.getString("name"));
                r.add(rs.getString("pay_month"));
                r.add(rs.getString("pay_year"));
                r.add(rs.getString("amount"));
                r.add(rs.getString("pay_date"));
                r.add(rs.getString("pay_mode"));
                dtm.addRow(r);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
