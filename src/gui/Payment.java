    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String rid=(String)renterID.getSelectedItem();
        int mon=m.getMonth();
        String month="";
        switch(mon){
            case 1: month="Jan";break;
            case 2: month="Feb";break;
            case 3: month="Mar";break;
            case 4: month="Apr";break;
            case 5: month="May";break;
            case 6: month="June";break;
            case 7: month="July";break;
            case 8: month="Aug";break;
            case 9: month="Sep";break;
            case 10: month="Oct";break;
            case 11: month="Nov";break;
            case 12: month="Dec";break;
        }
        String name=n.getText();
        String year=y.getYear()+"";
        int amount=Integer.parseInt(a.getText());
        String payMode=(String)p.getSelectedItem();
        try{
        db.DbConnect.checkMonthYear.setString(1, month);
        db.DbConnect.checkMonthYear.setString(2, year);
        db.DbConnect.checkMonthYear.setInt(3, Integer.parseInt(rid));
        ResultSet rs=db.DbConnect.checkMonthYear.executeQuery();
        if(!rs.next()){
        db.DbConnect.insertPayment.setInt(1,Integer.parseInt(rid));
        db.DbConnect.insertPayment.setString(2, name);
        db.DbConnect.insertPayment.setString(3, month);
        db.DbConnect.insertPayment.setString(4, year);
        db.DbConnect.insertPayment.setInt(5, amount);
        db.DbConnect.insertPayment.setString(6, payMode);
        db.DbConnect.insertPayment.executeUpdate();
        JOptionPane.showMessageDialog(null, "Payment Done !");
        }else{
            JOptionPane.showMessageDialog(null, "Already Paid for this Month !");
        }
        getEntries();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

