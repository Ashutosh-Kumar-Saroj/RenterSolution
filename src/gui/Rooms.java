    void getEntries(){
        try{
            javax.swing.table.DefaultTableModel dtm=
        (javax.swing.table.DefaultTableModel)rTable.getModel();
            int rc=dtm.getRowCount();
            while(rc--!=0){
                dtm.removeRow(0);
            }
            ResultSet rs=db.DbConnect.getRooms.executeQuery();
            while(rs.next()){
               Vector r=new Vector();
               r.add(rs.getInt("floor"));
               r.add(rs.getString("room_no"));
               r.add(rs.getInt("price"));
               r.add(rs.getString("ac"));
               r.add(rs.getString("type"));
               r.add(rs.getInt("vacant_bed"));
               dtm.addRow(r);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
