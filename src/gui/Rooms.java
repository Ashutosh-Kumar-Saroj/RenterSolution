    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String floor=(String)f.getSelectedItem();
        String room=r.getText();
        String pr=p.getText();
        String ac;
        if(ac1.isSelected())
            ac=ac1.getText();
        else 
            ac=ac2.getText();
        String type=(String)t.getSelectedItem();
        if(!room.equals("") && !pr.equals("")){
            try{
                int v=0;
                if(type.equalsIgnoreCase("single"))
                    v=1;
                else if(type.equalsIgnoreCase("double"))
                    v=2;
                else if(type.equalsIgnoreCase("triple"))
                    v=3;
                int price=Integer.parseInt(pr);
                db.DbConnect.insertRoom.setString(1, room);
                db.DbConnect.insertRoom.setInt(2, price);
                db.DbConnect.insertRoom.setString(3, ac);
                db.DbConnect.insertRoom.setString(4, type);
                db.DbConnect.insertRoom.setString(5, floor);
                db.DbConnect.insertRoom.setInt(6, v);
                db.DbConnect.insertRoom.executeUpdate();
                getEntries();
                JOptionPane.showMessageDialog(null, "Successfully Added");
            }catch(java.sql.SQLIntegrityConstraintViolationException e){
                JOptionPane.showMessageDialog(null, "Room Already exist!");
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int ri=rTable.getSelectedRow();
        if(ri!=-1){
            try{
                int x=JOptionPane.showConfirmDialog(null,"Do u really wanna Delete","Deletion Confirmation", JOptionPane.YES_NO_OPTION);
                if(x==JOptionPane.YES_OPTION){
                    String rn=(String)rTable.getValueAt(ri, 1);
                    db.DbConnect.st.executeUpdate("delete from rooms where room_no='"+rn+"'");
                    getEntries();
                    JOptionPane.showMessageDialog(null, "Successfully Deleted!");
                }
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Plz Select the room first!");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

